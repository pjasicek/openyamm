constexpr uint32_t spellIdValue(SpellId spellId)
{
    return static_cast<uint32_t>(spellId);
}

constexpr SpellId spellIdFromValue(uint32_t spellId)
{
    return static_cast<SpellId>(spellId);
}

constexpr bool isSpellId(uint32_t rawSpellId, SpellId spellId)
{
    return rawSpellId == spellIdValue(spellId);
}

constexpr bool spellIdInRange(uint32_t rawSpellId, SpellId first, SpellId last)
{
    return rawSpellId >= spellIdValue(first) && rawSpellId <= spellIdValue(last);
}
